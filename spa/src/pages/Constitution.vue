            member; the Founders do not require a vote.</li>
          <li>Grounds for dismissal are as follows:
            <ol class=" p-5 list-disc">
              <li>Inactivity. Not regularly engaging in Council votes, discussions, and meetings.</li>
              <li>Conduct. Harassing, bullying, or demeaning other Council members.</li>
              <li>Dereliction of duty. Not fulfilling the duties of your position pertaining to your role in Council and
                your role outside of Council (for example, a Colony Leader not fulfilling their duties as CL).</li>
              <li>Divulging confidential Council information to unauthorized individuals.</li>
              <li>Or a breach of anything in Part 3 of this document.</li>
            </ol>
          </li>
        </ol>

        <h2>4. SECTION 4: Decorum, Debates, and Voting</h2>
        <ol class="p-5">
          <li><strong>Decorum</strong>
            <ol class="p-5 list-disc">
              <li>All members of the City Council shall tacitly agree to follow this constitution. It is the
                responsibility of each member to familiarize themselves with the rules and guidelines of this document.
              </li>
              <li>As a deliberative body, the City Council expects and encourages lively debate; however, it always
                requires courtesy, politeness, and respect.</li>
              <li>All City Council affairs, past or present, are considered confidential, and sharing any
                Council-related information not available to the public with an unauthorized non-council member is
                strictly forbidden.</li>
            </ol>
          </li>

          <li><strong>Debates</strong>
            <ol class="p-5 list-disc">
              <li>Any City Council member can propose an issue to be debated upon. This should be done in the official
                CTR Council Discord, and a separate post should be created for the issue within the Council Chambers
                forum.</li>
              <li>The Senior City Council Member is responsible for moderating debates and keeping them on track. They
                will also call a vote once they feel the debate has reached its completion.</li>
            </ol>
          </li>

          <li><strong>Voting</strong>
            <ol class="p-5 list-disc">
              <li>Once a debate has concluded, the City Council will have an opportunity to vote.</li>
              <li>All votes will be done via Discord and once one has been introduced it will be open for three (3)
                days. If all Council members have voted before the three (3) day period has expired then the vote can
                close early.</li>
              <li>If the Senior Council Member deems necessary, the vote time frame will be shortened to twenty-four
                (24) hours for emergency votes. This will be communicated during the debate and when the vote is posted
                for council.</li>
              <li>The Senior City Council Member will call the votes and announce the outcomes.</li>
              <li>The Senior City Council Member will only vote in the event of a tie. They will cast the tie-breaking
                vote.</li>
              <li>If the Mayor vetoes a vote, the Council may nullify the Mayoral veto with a unanimous vote of ALL
                Council members, less that of the Mayor.</li>
            </ol>
          </li>
        </ol>

