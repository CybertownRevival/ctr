              <li>The ability to approve any citizen petitioning for an honorary title.</li>
              <li>The ability to open or close any Colony or Place.</li>
              <li>The ability to amend this constitution.</li>
              <li>The ability to create or remove any job or title within the city.</li>
            </ol>
          </li>
