            <li>Inactivity. Failing to log into CTR and/or participate in the community on Discord.</li>
            <li>Bullying, harassing, or disrespecting other members including City Council Members.</li>
            <li>Divulging confidential Council information to unauthorized individuals.</li>
            <li>Or a breach of anything in Part 3 of this document.</li>
          </ul>
          <p>A formal statement must be drawn up by a Council Member clearly stating reasons and examples for
            impeachment. Once submitted, the Council will have one (1) week to investigate and deliberate and then one
            (1) additional week to vote on impeachment.</p>
