        <h3>ROLE OF THE FOUNDERS</h3>
        <ol class="p-5 list-disc">
          <li>The founders serve as an invisible hand within the city supporting the community and empowering the City
            Council to manage the daily workings of Cybertown Revival.</li>
          <li>The founders of CTR hold executive power within the city and have the ability to veto any ruling made by
            the City Council or Mayor.</li>
          <li>If necessary, the founders have the authority to remove City Council members or the Mayor.</li>
          <li>Upon its first formation and any subsequent required reformation, the founders will choose a group of
            citizens to form the City Council.</li>
        </ol>

        <h3>ROLE OF THE CITY COUNCIL</h3>
        <p class="p-5">The City Council is the legislative and policymaking body of Cybertown Revival. They provide
          oversight for the colonies and places within the city and act in the best interest of the future of CTR.</p>

        <h2>1. SECTION 1: Composition of the City Council</h2>
        <ol class="p-5 list-disc">
          <li>Colony Representative – represents the Colony Leaders and the interests of all the colonies</li>
          <li>Places Representative – represents the Places Leaders and the interests of all the places</li>
          <li>Security Chief – represents the rule of law within the city</li>
          <li>City Advisor – represents the best interests of the city as a whole</li>
          <li>Senior City Council Member – a current council member who is elected by the council to lead the council in
            policy discussions, meetings, and admin.</li>
        </ol>

        <h2>2. SECTION 2: Admittance into the City Council</h2>
        <ol class="p-5">
          <li>The initial formation of the City Council will be decided by the founders. After that is established, the
            roles are chosen as follows:</li>
        </ol>

        <div>
          <p><strong>(1) Colony Representative –</strong></p>
          <div class="p-5">
            <p>(a) A candidate for Colony Representative must be a current Colony Leader who is active and in good
              standing within the community. New candidates will be interviewed and voted in by the Council by a
              majority vote.</p>
            <p>(b) The Colony Representative oversees the Colony Leaders and makes sure the colonies are running
              smoothly. Any colony matters that need council attention will be presented by the Colony Representative.
            </p>
            <p>(c) The Colony Representative will interview candidates for Colony Leader when there are vacant
              positions. The candidate must then acquire a majority vote from the Council to be hired.</p>
          </div>

          <p><strong>(2) Places Representative –</strong></p>
          <div class="p-5">
            <p>(a) A candidate for Places Representative must be a current Places Chief who is active and in good
              standing within the community. New candidates will be interviewed and voted in by the Council by a
              majority vote.</p>
            <p>(b) The Places Representative oversees all of the Places within the city as well as the chiefs.</p>
            <p>(c) The Places Representative will interview and hire new Places Chiefs when vacancies occur. Places
              Chiefs do not require a Council vote.</p>
          </div>

          <p><strong>(3) Security Chief –</strong></p>
          <div class="p-5">
            <p>(a) A candidate for Security Chief must be a current and active member of the security force. New
              candidates will be interviewed and voted in by the Council by a majority vote.</p>
            <p>(b) The Security Chief oversees the Security force within the city.</p>
            <p>(c) The Security Chief decides which citizens receive security tools.</p>
          </div>

          <p><strong>(4) City Advisor –</strong></p>
          <div class="p-5">
            <p>(a) Candidates for City advisor will be nominated by the City Council. They are chosen for their
              experience and wisdom within the city. They might be past Mayors, Colony Leaders, CERB, Security, or
              Places Chiefs. They may also hold another job within the city while a City Advisor (ie. Colony Leader or
              Places Chief).</p>
          </div>

          <p><strong>(5) Senior City Council Member –</strong></p>
          <div class="p-5">
            <p>(a) This role will be chosen by the Council from the existing Council members. They will serve a two (2)
              year term as Senior City Council. There are no term limits.</p>
            <p>(b) The Senior City Council Member acts as the leadership of the council, calling votes, initiating
              discussion, and overseeing the integrity of the Council.</p>
          </div>
        </div>

        <h2>3. SECTION 3: Dismissal From the Council</h2>
        <ol class="p-5">
          <li>The Founders and the Senior City Council Member reserve the right to dismiss a member of City Council if
