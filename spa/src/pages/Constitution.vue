        <div class="p-5">
          <p>To qualify to run for Mayor, applicants must:</p>
          <ul class="p-5 list-disc">
            <li>Have been a citizen in CTR for at least one (1) year.</li>
            <li>Have a minimum of 1000 experience points.</li>
            <li>Have a clean security record.</li>
            <li>Be 18 years or older.</li>
          </ul>
          <p>The Mayor’s term will last one (1) year.</p>
          <p class="py-5">The Mayor may not hold office for more than two (2) consecutive terms.</p>
          <p class="py-5">Once elected, the Mayor may NOT hold any other position in CTR.</p>

        </div>

