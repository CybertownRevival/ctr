      <div class="px-10">
        <h3>ROLE OF C.E.R.B.</h3>
        <div class="p-5">
          <p>This constitution grants all judicial power(s) to C.E.R.B., and, in doing so, the members of C.E.R.B. shall
            ensure equal representation to all citizens of CTR within the boundaries of this constitution.</p>

        </div>

        <h2>SECTION 1: Composition of C.E.R.B.</h2>
        <div class="p-5">
          <p>C.E.R.B. Chief: the head of the judicial branch in charge of hiring/removing C.E.R.B. members.</p>
          <p class="py-5">C.E.R.B. Member: There can be two (2) to four (4) members, aside from the chief, at any given
            time and at the discretion of the C.E.R.B. Chief. There should always be an even number of members (not
            including the chief).</p>

        </div>

        <h2>SECTION 2: Qualifications of C.E.R.B.</h2>
        <div class="p-5">
          <ul class="p-5 list-disc">
            <li>Must be 18 years or older.</li>
            <li>Be a citizen for at least one (1) year.</li>
            <li>Have a minimum of 1000 exp.</li>
            <li>Clean security record.</li>
            <li>Upon accepting a position on C.E.R.B. the member may not hold any other job in Cybertown Revival.</li>
            <li>A deep understanding and knowledge of the CTR Constitution.</li>
          </ul>
        </div>

        <h2>SECTION 3: Admittance into C.E.R.B.</h2>
        <div class="p-5">
          <p>C.E.R.B. Chief: this position is chosen by the City Council via a vote. Members are nominated by Council
            members and then a vote is held to determine the best suited individual.</p>
          <p class="py-5">C.E.R.B. Member: Once a Chief is established, the Chief will be tasked with hiring and
            overseeing members of C.E.R.B.</p>

        </div>

        <h2>SECTION 4: Dismissal of C.E.R.B. Membership</h2>
        <div class="p-5">
          <p>Once a member joins C.E.R.B. they remain a member until they resign or are removed by the C.E.R.B. Chief or
            City Council.</p>
          <p class="py-5">The City Council may remove the sitting C.E.R.B. Chief if there is a breach of conduct.</p>
          <p class="py-5">C.E.R.B. membership might be revoked for:</p>
          <ul class="px-5 list-disc">
            <li>Inactivity or prolonged absenteeism.</li>
            <li>Any infraction from Part 3 of this document.</li>
            <li>Misconduct or corruption.</li>
            <li>Divulging confidential information pertaining to C.E.R.B. cases.</li>
          </ul>

        </div>

        <h2>SECTION 5: C.E.R.B. Jurisdiction</h2>
        <div class="p-5">
          <p>The following is a non-exhaustive list of the powers reserved for C.E.R.B.:</p>
          <ul class="p-5 list-disc">
            <li>The ability to hear, investigate, deliberate, and judge any case, claim, or appeal that comes before
              them, whether from an active or banned citizen.</li>
            <li>The ability to change, overturn, shorten, or extend any punishment delivered by security by utilizing
              and interpreting this constitution.</li>
            <li>The ability to hear, investigate, deliberate, and judge any articles of impeachment towards the
              Mayorship or members of City Council</li>
            <li>C.E.R.B. has the authority to decide the constitutionality of laws or proposals passed by the City
              Council.</li>
            <li>Clear a citizen’s security record after one (1) year since the infraction.</li>
          </ul>

          <p>C.E.R.B. may NOT:</p>
          <ul class="p-5 list-disc">
            <li>Interfere with the inner workings of the Colonies or City places to undermine the authority of the
              acting Colony Leader’s or Place Leader’s sovereignty unless a breach of this constitution occurs.</li>
            <li>Interfere with the election process once one has begun.</li>
            <li>Interfere in the City Council’s decisional capacity or legislative authority unless a breach of this
              Constitution occurs.</li>
          </ul>
        </div>

        <h2>SECTION 6: Arbitration</h2>
        <div class="p-5">
          <p>If a citizen feels a ruling against them is unjust or violates the constitution, they may submit an appeal
            to C.E.R.B.</p>
          <p class="py-5">Citizens have two (2) weeks from the start of a ban to file an appeal. Appeals filed after
            that period will not be accepted.</p>
          <p class="py-5">Citizens may also submit a request to clear their security record after a minimum of one (1)
            year since the infraction. This request will be treated the same as the appeal process.</p>
          <p class="py-5">If the request is denied, the citizen must wait a minimum of six (6) months before submitting
            another request.</p>
          <p class="py-5">Once an appeal has been submitted, C.E.R.B. will gather the appropriate documents from the
            citizen, Security, City Council, and/or any other necessary source.</p>
          <p class="py-5">The C.E.R.B. Board will have two (2) weeks from the time all information has been submitted to
            come to a verdict.</p>
          <p class="py-5">Once deliberation between C.E.R.B. members has finished, the C.E.R.B. Chief will call for a
            vote.</p>
          <p class="py-5">A majority vote is required for an appeal to be approved.</p>
          <p class="py-5">The Chief will lead the investigation but will not vote unless there is a need for a
            tiebreaker.</p>
          <p class="py-5">C.E.R.B. decisions are final and cannot be overturned or appealed further.</p>

        </div>

        <h2>SECTION 7: Citizen v. Citizen Arbitration</h2>
