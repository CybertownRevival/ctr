        <h2>SECTION 1: ELECTION PROCESS</h2>
        <div class="p-5">
          <p class="py-5">Elections will take place in the beginning of the year, with an exact date being set by the
            City Council.</p>
          <p class="py-5">Applications will open up to the city for a time period of two (2) weeks. These applications
            will be submitted to the City Council for vetting.</p>
          <p class="py-5">Once candidates have been chosen, the campaign will last for four (4) weeks. During this
            period the City Council will schedule Mayoral debates.</p>
          <p class="py-5">At the end of the campaign period, a vote shall be held for one (1) week. All citizens of
            Cybertown Revival may vote (alt accounts may NOT vote).</p>

        </div>

