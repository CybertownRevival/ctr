        <div class="p-5">
          <p>The role of the Mayor is largely a figurehead position. They will act as an ambassador for the city.</p>
          <ul class="p-5 list-disc">
            <li>The Mayor will sit as a non-voting member of the City Council.</li>
            <li>The Mayor can present policy changes to the Council for debate and voting.</li>
            <li>The Mayor has the power to veto a City Council vote.</li>
          </ul>

          <p>The Mayor may NOT:</p>
          <ul class="p-5 list-disc">
            <li>Interfere with the inner workings of the Colonies or City places to undermine the authority of the
